package com.corejavaRehanaMaam;

public class Version1 extends AbstractCalciver1 {
	@Override
	int add(int a,int b) {
		return a+b;
	}
	@Override
	int sub(int a,int b) {
		return a-b;
	}
	int add(int a,int b,int c) {
		return a+b+c;
	}
	int div(int a,int b) {
		return a/b;
	}
	
	int mul(int a,int b) {
		return a*b;
}
}
